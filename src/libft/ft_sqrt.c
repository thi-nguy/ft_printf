int		ft_sqrt(int nb)
{
	int i;
	int r;

	i = 1;
	r = nb % 10;
	if (nb > 2147395600 || nb < 0 || nb == 0)
		return (0);
	if ((r != 0) && (r != 1) && (r != 4) && (r != 5) && (r != 6) && (r != 9))
		return (0);
	while (i * i <= nb)
	{
		if ((i * i) == nb)
			return (i);
		i++;
	}
	return (0);
}
